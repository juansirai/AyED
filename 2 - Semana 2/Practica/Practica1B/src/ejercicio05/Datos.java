package ejercicio05;

public class Datos {
	private int min;
	private int max;
	private double avg;
	
	// Setters y Getters
	public double getAvg() {
		return avg;
	}
	public int getMax() {
		return max;
	}
	public int getMin() {
		return min;
	}
	public void setAvg(double avg) {
		this.avg = avg;
	}
	public void setMax(int max) {
		this.max = max;
	}
	public void setMin(int min) {
		this.min = min;
	}
	
	
}
